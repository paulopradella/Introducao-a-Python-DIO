#Constituindo Métodos, funções e classe em Python
#Criando uma função
def soma(a, b):
    return a + b

def subtracao(a, b):
    return a - b

def multiplicacao(a, b):
    return a * b

def divisao(a, b):
    return a / b

print(soma(1, 2))
print(subtracao(10, 2))
print(multiplicacao(1, 2))
print(divisao(10, 2))
